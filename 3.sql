Select Заказы.Дата, dbo.Товары.Цена, dbo.Заказчики.Наименование, dbo.Доставка.[Способ доставки]
From  Заказы, Товары, Заказчики, Доставка
Where Заказы.[Код товара] = Товары.[Код товара] and
      Заказы.[Код заказчика] = Заказчики.[Код заказчика] and
	  Заказы.[Код доставки] = Доставка.[Код доставки]